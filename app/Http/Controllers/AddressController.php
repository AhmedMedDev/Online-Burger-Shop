     */
    public function store(Request $request)
    {
        $request = $request->only([
            'name',
            'country',
            'street',
            'city',
            'postcode',
            'phone',
            'order_notes',
            'user_id'
        ]);

        $address = Address::create( $request );

        return response([
            'status'=> true,
            'data' => $address
        ]);
    }

